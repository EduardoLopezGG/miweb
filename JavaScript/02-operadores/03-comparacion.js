let a = 10;

//Operadores de Comparacion Relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//Operadores de Comparacion de Igualdad

console.log(a == 10); //igual
console.log(a != 10); //no es igual
console.log(a === '10'); //igual y compara si tienen el mismo tipo de dato
console.log(a !== '10'); //igual y compara si tiene un tipo de dato distinto
